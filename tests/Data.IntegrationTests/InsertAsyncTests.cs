using CodeArchitects.Platform.Data.Fixtures;
using CodeArchitects.Platform.Data.Fixtures.Model;
using Microsoft.EntityFrameworkCore;
using Xunit.Abstractions;

namespace CodeArchitects.Platform.Data;

[Collection(TestCollection.Name)]
public class InsertAsyncTests : TestBase
{
  public InsertAsyncTests(TestFixture fixture, ITestOutputHelper output)
    : base(fixture, output)
  {
  }

  [Theory, RepositoryDependenciesData]
  public async Task InsertAsync_ShouldSetAutogeneratedColumn_WhenEntityHasAutogeneratedColumn(RepositoryDependencies dependencies)
  {
    // Arrange
    SerialEntity entity = SerialEntity.One();

    using var dbFixture = _fixture.CreateDbFixture(dependencies);
    using var scope = _fixture.CreateScope(dependencies);
    var sut = scope.CreateRepository<SerialEntity, int>();

    // Act
    await sut.InsertAsync(entity);

    // Assert
    SerialEntity? fromDb = scope.DbContext.Set<SerialEntity>()
      .FirstOrDefault(x => x.Id == entity.Id);

    fromDb.Should().NotBeNull();
    entity.Id.Should().NotBe(0).And.Be(fromDb!.Id);
  }

  [Theory, RepositoryDependenciesData]
  public async Task InsertAsync_ShouldInsertEntityAndAddLinks_WhenNavigationIsManyToMany(RepositoryDependencies dependencies)
  {
    // Arrange
    Typology typology = Typology.One();
    Category category = Category.One();
    category.Typologies = new() { typology };

    using var dbFixture = _fixture.CreateDbFixture(dependencies);
    dbFixture.Seed(seeder => seeder.Seed(typology));
    using var scope = _fixture.CreateScope(dependencies);
    var sut = scope.CreateRepository<Category, Guid>();

    // Act
    await sut.InsertAsync(category);

    Category? fromDb = scope.DbContext.Set<Category>()
      .Include(x => x.Typologies)
      .FirstOrDefault(x => x.Id == category.Id);

    // Assert
    fromDb.Should().NotBeNull();
    fromDb!.Typologies.Should().HaveCount(1)
      .And.ContainSingle(t => t.Id == typology.Id && t.Name == typology.Name);
  }

  [Theory, RepositoryDependenciesData]
  public async Task InsertAsync_ShouldInsertEntityAndAddLinks_WhenNavigationIsOneToManyInterAggregate(RepositoryDependencies dependencies)
  {
    // Arrange
    Customer customer = Customer.One();
    Cart cart = Cart.One();
    cart.Items = CartItem.Many(1, cart.Id);
    customer.Carts = new() { cart };

    using var dbFixture = _fixture.CreateDbFixture(dependencies);
    dbFixture.Seed(seeder => seeder.Seed(cart));
    using var scope = _fixture.CreateScope(dependencies);
    var sut = scope.CreateRepository<Customer, Guid>();

    // Act
    await sut.InsertAsync(customer);

    Customer? fromDb = scope.DbContext.Set<Customer>()
      .Include(x => x.Carts)
      .FirstOrDefault(x => x.Id == customer.Id);

    // Assert
    fromDb.Should().NotBeNull();
    fromDb!.Carts.Should().HaveCount(1)
      .And.ContainSingle(c => c.Id == cart.Id && c.Name == cart.Name);
  }

