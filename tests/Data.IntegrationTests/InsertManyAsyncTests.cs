  [Theory, RepositoryDependenciesData]
  public async Task InsertManyAsync_ShouldInsertEntityAndChildren_WhenNavigationIsOneToManyIntraAggregate(RepositoryDependencies dependencies)
  {
    // Arrange
    List<Cart> carts = Cart.Many(2);
    CartItem item0 = CartItem.One(carts[0].Id);
    carts[0].Items = new() { item0 };
    CartItem item1 = CartItem.One(carts[1].Id);
    carts[1].Items = new() { item1 };

    var sut = _fixture.CreateRepository<Cart, Guid>(dependencies);

    // Act
    await sut.InsertManyAsync(carts);

    // Assert
    IQueryable<Cart> queryable = _fixture.DbContext.Set<Cart>().Include(x => x.Items);
    Cart?[] fromDb = new[]
    {
      queryable.FirstOrDefault(x => x.Id == carts[0].Id),
      queryable.FirstOrDefault(x => x.Id == carts[1].Id)
    };

    fromDb[0].Should().NotBeNull();
    fromDb[0]!.Items.Should().HaveCount(1)
      .And.ContainSingle(i => i.Index == item0.Index && i.CartId == item0.CartId && i.Name == item0.Name);
    fromDb[1].Should().NotBeNull();
    fromDb[1]!.Items.Should().HaveCount(1)
      .And.ContainSingle(i => i.Index == item1.Index && i.CartId == item1.CartId && i.Name == item1.Name);
  }

