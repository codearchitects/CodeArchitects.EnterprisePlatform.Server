using CodeArchitects.Platform.Data.AdoNet.Model.Implementation;
using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;
using System.Linq.Expressions;
using System.Reflection;

namespace CodeArchitects.Platform.Data.AdoNet.Model.Builder;

internal class EntityModelBuilder<TEntity> : EntityModelBuilder, IEntityModelBuilder<TEntity>
  where TEntity : class
{
  private readonly List<string> _keyNames;
  private readonly HashSet<MemberInfo> _ignoredMembers;
  private readonly Dictionary<MemberInfo, string> _columnNames;
  private readonly Dictionary<Name, INavigationModel> _navigations;
  private MemberInfo? _autoGeneratedColumn;
  private string? _tableName;
  private EntityModel? _entity;
  private PrimaryKeyModel? _primaryKey;
  private InitializerModel? _initializer;
  private IReadOnlyCollection<MemberInfo>? _primaryKeyMembers;

  public EntityModelBuilder(string entityName)
    : base(entityName)
  {
    _keyNames = new();
    _ignoredMembers = new();
    _columnNames = new();
    _navigations = new();
  }

  [MemberNotNullWhen(true, nameof(_entity), nameof(_primaryKey), nameof(_initializer), nameof(_primaryKeyMembers))]
  private bool HasCompleted { get; set; }

  public override Type EntityType => typeof(TEntity);

  public IEntityModelBuilder<TEntity> Ignore(string memberName)
  {
    MemberInfo member = GetMember(EntityType, memberName);

    _ignoredMembers.Add(member);
    return this;
  }

  public IEntityModelBuilder<TEntity> Ignore<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetMemberAndTypeFromExpression(expression.Body, out _autoGeneratedColumn, out _))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn(string memberName)
  {
    _autoGeneratedColumn = GetMember(EntityType, memberName);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetMemberAndTypeFromExpression(expression.Body, out MemberInfo? member, out _))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _ignoredMembers.Add(member);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(Expression<Func<TEntity, TProperty>> expression, string columnName)
  {
    if (!TryGetMemberAndTypeFromExpression(expression.Body, out MemberInfo? member, out _))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(string memberName, string columnName)
  {
    MemberInfo member = GetMember(EntityType, memberName);
    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey(params string[] keyNames)
  {
    CheckKeyArity(keyNames.Length);

    _keyNames.Clear();

    foreach (string keyName in keyNames)
    {
      (MemberInfo member, Type memberType) = GetMemberAndType(EntityType, keyName);
      CheckIEquatable(member, memberType);

      _keyNames.Add(keyName);
    }

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey<TKey>(Expression<Func<TEntity, TKey>> expression)
  {
    _keyNames.Clear();

    IEnumerable<string> keyNames = GetKeyNames(expression.Body)
      .Select(name => name.IsColumnName
        ? throw new ModelConfigurationException($"'{nameof(ModelConfiguration)}.{nameof(ModelConfiguration.Column)}' is not supported for primary keys.")
        : name.Value);

    _keyNames.AddRange(keyNames);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithTableName(string tableName)
  {
    _tableName = tableName;

    return this;
  }

  public override EntityModel Build()
  {
    Debug.Assert(!HasCompleted, "Builder had already completed.");
    HasCompleted = true;

    IReadOnlyList<(MemberInfo, Type)> keyMembersAndTypes = GetKeyMembersAndTypes();
    _primaryKeyMembers = keyMembersAndTypes
      .Select(pair => pair.Item1)
      .ToList();

    _initializer = InitializerModel.Create(EntityType);
    _primaryKey = PrimaryKeyModel.Create(keyMembersAndTypes);

    _entity = EntityModel.Create(EntityType, _initializer, _primaryKey);
    return _entity;
  }

  public override void AddNavigation(INavigationModel navigation, IEnumerable<Name> foreignKeyNames)
  {
    if (!HasCompleted)
      throw new InvalidOperationException("Builder has not completed yet.");

    foreach (Name foreignKeyName in foreignKeyNames)
    {
      _navigations.Add(foreignKeyName, navigation);
    }
    _entity.AddNavigation(navigation);
  }

  public override void AddColumns()
  {
    if (!HasCompleted)
      throw new InvalidOperationException("Builder has not completed yet.");

    IReadOnlyList<AccessibleMemberComponent<object?>> primaryKeyMemberComponents = _primaryKeyMembers
      .Select(AccessibleMemberComponent<object?>.Create)
      .ToList();

    short index = 0;
    foreach (var memberComponent in primaryKeyMemberComponents)
    {
      IColumnModel column = _navigations.TryGetValue(memberComponent.Name, out INavigationModel? navigation)
        ? new PrimaryAndForeignKeyColumnModel(memberComponent, index, index, navigation)
        : new PrimaryKeyColumnModel(memberComponent, index, index);

      _entity.AddColumn(column);
      index++;
    }

    IReadOnlyList<AccessibleMemberComponent<object?>> otherMemberComponents = EntityType.GetMembers(BindingFlags.Instance | BindingFlags.Public)
      .Where(member =>
        member.MemberType is MemberTypes.Property or MemberTypes.Field &&
        !_primaryKeyMembers.Contains(member) &&
        !_entity.Navigations.Any(pair => pair.Member == member) &&
        !_ignoredMembers.Contains(member))
      .Select(AccessibleMemberComponent<object?>.Create)
      .ToList();

    foreach (var memberComponent in otherMemberComponents)
    {
      IColumnModel column = _navigations.TryGetValue(memberComponent.Name, out INavigationModel? navigation)
        ? new AccessibleForeignKeyColumnModel(memberComponent, index, navigation)
        : new OrdinaryColumnModel(memberComponent, index);

      _entity.AddColumn(column);
      index++;
    }
  }

  private IReadOnlyList<(MemberInfo, Type)> GetKeyMembersAndTypes()
  {
    if (_keyNames.Count != 0)
    {
      return _keyNames
        .Select(name => GetMemberAndType(EntityType, name))
        .ToList();
    }

    try
    {
      (MemberInfo, Type) memberAndType = GetMemberAndType(EntityType, "Id");
      return new[] { memberAndType };
    }
    catch (ModelConfigurationException)
    {
      throw new ModelConfigurationException($"Could not find a default key for entity of type '{EntityType}'");
    }
  }

  private static void CheckIEquatable(MemberInfo member, Type memberType)
  {
    if (!typeof(IEquatable<>).MakeGenericType(memberType).IsAssignableFrom(memberType))
      throw new ModelConfigurationException($"Key member '{member.Name}' does not implement the IEquatable interface.");
  }
}
