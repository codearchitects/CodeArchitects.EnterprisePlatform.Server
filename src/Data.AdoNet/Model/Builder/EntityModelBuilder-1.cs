using CodeArchitects.Platform.Data.AdoNet.Model.Implementation;
using System.Linq.Expressions;
using System.Reflection;

namespace CodeArchitects.Platform.Data.AdoNet.Model.Builder;

internal class EntityModelBuilder<TEntity> : EntityModelBuilder, IEntityModelBuilder<TEntity>
  where TEntity : class
{
  private readonly List<Name> _keyNames;
  private readonly HashSet<MemberInfo> _ignoredMembers;
  private readonly Dictionary<MemberInfo, string> _columnNames;
  private MemberInfo? _autoGeneratedColumn;
  private string? _tableName;

  public EntityModelBuilder(string entityName)
    : base(entityName)
  {
    _keyNames = new();
    _ignoredMembers = new();
    _columnNames = new();
  }

  public override Type EntityType => typeof(TEntity);

  public override IEntityModel Build(IEnumerable<Association> associationsTo)
  {
    List<AccessibleMemberModelComponent> memberComponents = EntityType
      .GetMembers(BindingFlags.Instance | BindingFlags.Public)
      .Where(member => !_ignoredMembers.Contains(member))
      .Select(AccessibleMemberModelComponent.Create)
      .ToList();


  }

  public IEntityModelBuilder<TEntity> Ignore(string memberName)
  {
    (MemberInfo member, _) = GetMemberAndType(typeof(TEntity), memberName);

    _ignoredMembers.Add(member);
    return this;
  }

  public IEntityModelBuilder<TEntity> Ignore<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetPropertyOrField(expression.Body, out _autoGeneratedColumn))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn(string memberName)
  {
    (_autoGeneratedColumn, _) = GetMemberAndType(typeof(TEntity), memberName);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetPropertyOrField(expression.Body, out MemberInfo? member))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _ignoredMembers.Add(member);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(Expression<Func<TEntity, TProperty>> expression, string columnName)
  {
    if (!TryGetPropertyOrField(expression.Body, out MemberInfo? member))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(string memberName, string columnName)
  {
    (MemberInfo member, _) = GetMemberAndType(typeof(TEntity), memberName);
    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey(params string[] keyNames)
  {
    _keyNames.Clear();

    foreach (string keyName in keyNames)
    {
      (MemberInfo member, Type memberType) = GetMemberAndType(typeof(TEntity), keyName);
      CheckIEquatable(member, memberType);

      _keyNames.Add(keyName);
    }

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey(params Name[] keyNames)
  {
    _keyNames.Clear();

    foreach (Name keyName in keyNames)
    {
      if (!keyName.IsColumnName)
      {
        (MemberInfo member, Type memberType) = GetMemberAndType(typeof(TEntity), keyName.Value);
        CheckIEquatable(member, memberType);
      }

      _keyNames.Add(keyName);
    }

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey<TKey>(Expression<Func<TEntity, TKey>> expression)
  {
    _keyNames.Clear();
    _keyNames.AddRange(GetKeyNames(expression.Body));

    return this;
  }

  public IEntityModelBuilder<TEntity> WithTableName(string tableName)
  {
    _tableName = tableName;

    return this;
  }

  private static void CheckIEquatable(MemberInfo member, Type memberType)
  {
    if (!typeof(IEquatable<>).MakeGenericType(memberType).IsAssignableFrom(memberType))
      throw new ModelConfigurationException($"Key member '{member.Name}' does not implement the IEquatable interface.");
  }
}
