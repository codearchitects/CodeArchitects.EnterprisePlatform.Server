using CodeArchitects.Platform.Data.AdoNet.Model.Implementation;
using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;
using System.Linq.Expressions;
using System.Reflection;

namespace CodeArchitects.Platform.Data.AdoNet.Model.Builder;

internal class EntityModelBuilder<TEntity> : EntityModelBuilder, IEntityModelBuilder<TEntity>
  where TEntity : class
{
  private readonly List<string> _keyNames;
  private readonly HashSet<MemberInfo> _ignoredMembers;
  private readonly Dictionary<MemberInfo, string> _columnNames;
  private MemberInfo? _autoGeneratedColumn;
  private string? _tableName;
  private IReadOnlyCollection<Type>? _entityTypes;
  private HashSet<Association>? _associationsFrom;
  private HashSet<Association>? _associationsTo;
  private EntityModel? _entityModel;
  private InitializerModel? _initializerModel;
  private PrimaryKeyModel? _primaryKeyModel;

  public EntityModelBuilder(string entityName)
    : base(entityName)
  {
    _keyNames = new();
    _ignoredMembers = new();
    _columnNames = new();
  }

  public override Type EntityType => typeof(TEntity);

  [MemberNotNullWhen(true, nameof(_entityModel), nameof(_initializerModel), nameof(_primaryKeyModel), nameof(_entityTypes), nameof(_associationsFrom), nameof(_associationsTo))]
  private bool HasCompleted { get; set; }

  // public override EntityModel EntityModel => _entityModel ?? throw new InvalidOperationException("Builder had not completed yet.");
  // 
  // public override PrimaryKeyModel PrimaryKeyModel => _primaryKeyModel ?? throw new InvalidOperationException("Builder had not completed yet.");

  public IEntityModelBuilder<TEntity> Ignore(string memberName)
  {
    MemberInfo member = GetMember(EntityType, memberName);

    _ignoredMembers.Add(member);
    return this;
  }

  public IEntityModelBuilder<TEntity> Ignore<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetPropertyOrField(expression.Body, out _autoGeneratedColumn))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn(string memberName)
  {
    _autoGeneratedColumn = GetMember(EntityType, memberName);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithAutoGeneratedColumn<TProperty>(Expression<Func<TEntity, TProperty>> expression)
  {
    if (!TryGetPropertyOrField(expression.Body, out MemberInfo? member))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _ignoredMembers.Add(member);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(Expression<Func<TEntity, TProperty>> expression, string columnName)
  {
    if (!TryGetPropertyOrField(expression.Body, out MemberInfo? member))
      throw new ModelConfigurationException("The expression must be 'x => x.Member'.");

    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithColumnName<TProperty>(string memberName, string columnName)
  {
    MemberInfo member = GetMember(EntityType, memberName);
    _columnNames[member] = columnName;

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey(params string[] keyNames)
  {
    CheckKeyArity(keyNames.Length);

    _keyNames.Clear();

    foreach (string keyName in keyNames)
    {
      (MemberInfo member, Type memberType) = GetMemberAndType(EntityType, keyName);
      CheckIEquatable(member, memberType);

      _keyNames.Add(keyName);
    }

    return this;
  }

  public IEntityModelBuilder<TEntity> WithKey<TKey>(Expression<Func<TEntity, TKey>> expression)
  {
    _keyNames.Clear();

    IEnumerable<string> keyNames = GetKeyNames(expression.Body)
      .Select(name => name.IsColumnName
        ? throw new ModelConfigurationException($"'{nameof(ModelConfiguration)}.{nameof(ModelConfiguration.Column)}' is not supported for primary keys.")
        : name.Value);

    _keyNames.AddRange(keyNames);

    return this;
  }

  public IEntityModelBuilder<TEntity> WithTableName(string tableName)
  {
    _tableName = tableName;

    return this;
  }

  public override void Complete(IReadOnlyCollection<Type> entityTypes, IEnumerable<Association> associations)
  {
    Debug.Assert(!HasCompleted, "Builder had already completed.");

    HasCompleted = true;
    _entityTypes = entityTypes;
    _associationsFrom = new(AssociationEqualityComparer.Instance);
    _associationsTo = new(AssociationEqualityComparer.Instance);

    foreach (Association association in associations)
    {
      if (association.From == EntityType)
      {
        _associationsFrom.Add(association);
      }

      if (association.To == EntityType)
      {
        _associationsTo.Add(association);
      }
    }

    List<(MemberInfo, Type)> keyMembersAndTypes = _keyNames
      .Select(name => GetMemberAndType(EntityType, name))
      .ToList();

    _initializerModel = InitializerModel.Create(EntityType);
    _primaryKeyModel = PrimaryKeyModel.Create(keyMembersAndTypes);
    _entityModel = EntityModel.Create(EntityType, _initializerModel, _primaryKeyModel);
  }

  private static void CheckIEquatable(MemberInfo member, Type memberType)
  {
    if (!typeof(IEquatable<>).MakeGenericType(memberType).IsAssignableFrom(memberType))
      throw new ModelConfigurationException($"Key member '{member.Name}' does not implement the IEquatable interface.");
  }
}
