using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;

namespace CodeArchitects.Platform.Data.AdoNet.Model.Implementation;

internal abstract class EntityModel : IEntityModel
{
  private string? _name;
  private string? _tableName;
  private readonly List<ColumnModel> _columns;
  private readonly List<INavigationModel> _navigations;
  private readonly List<IForeignKeyModel> _foreignKeys;

  public EntityModel(IInitializerModel initializer)
  {
    Initializer = initializer;
    _columns = new();
    _navigations = new();
    _foreignKeys = new();
  }

  [AllowNull]
  public string Name
  {
    get => _name ?? Type.Name;
    set => _name = value;
  }

  [AllowNull]
  public string TableName
  {
    get => _tableName ?? Type.Name;
    set => _tableName = value;
  }

  public abstract Type Type { get; }

  public IInitializerModel Initializer { get; }

  public IAccessibleColumnModel? AutoGeneratedColumn { get; set; }

  public IReadOnlyCollection<IForeignKeyModel> ForeignKeys => _foreignKeys;

  public IReadOnlyCollection<IColumnModel> Columns => _columns;

  public IReadOnlyCollection<INavigationModel> Navigations => _navigations;

  public IPrimaryKeyModel PrimaryKey => PrimaryKeyCore;

  protected abstract IPrimaryKeyModel PrimaryKeyCore { get; }

  public void AddColumn(ColumnModel column)
  {
    _columns.Add(column);
  }

  public bool TryGetNavigation(ReadOnlySpan<char> name, [NotNullWhen(true)] out IAccessibleNavigationModel? navigationModel)
  {
    foreach (INavigationModel navigation in Navigations)
    {
      if (!navigation.HasMember)
        continue;

      Debug.Assert(navigation is IAccessibleNavigationModel, $"A navigation model with member should be assignable to {nameof(IAccessibleNavigationModel)}.");

      if (name.SequenceEqual(navigation.Member.Name.AsSpan()))
      {
        navigationModel = (IAccessibleNavigationModel)navigation;
        return true;
      }
    }

    navigationModel = null;
    return false;
  }
}
